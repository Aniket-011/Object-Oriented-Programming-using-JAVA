
   public class Child {
      int age;
      int roll;
   
      Child(){  //Constructor with out argument 
         this.age = 20;
         this.roll = 15;
        
      }
   
      Child(int x , int y){  // Constructor with two argument 
         age = x;
         roll = y;
      }
   
      Child(int both){ // Constructor with one argument 
         age = both;
         roll = both;
      }
   
   
      
   }

